#!/usr/bin/env node
import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const LIST_PATH = join(__dirname, 'tabler-icons.json');
const OUT_PATH = join(ROOT, 'src/lib/components/ui/tabler-icons.ts');
const TABLER_ROOT = join(ROOT, 'node_modules/@tabler/icons/icons/outline');

async function main() {
  const list = JSON.parse(await readFile(LIST_PATH, 'utf-8'));
  if (!Array.isArray(list.icons) || list.icons.length === 0) {
    console.log('No icons listed in tabler-icons.json — writing empty file.');
    await writeFile(OUT_PATH, '// auto-generated by scripts/generate-tabler-icons.mjs — do not edit\nexport {};\n', 'utf-8');
    return;
  }

  const entries = [];
  for (const name of list.icons) {
    const svgPath = join(TABLER_ROOT, `${name}.svg`);
    let svg;
    try {
      svg = await readFile(svgPath, 'utf-8');
    } catch (err) {
      console.error(`Missing Tabler icon: ${name} (expected at ${svgPath})`);
      process.exit(1);
    }
    const inner = svg.replace(/^[\s\S]*?<svg[^>]*>/, '').replace(/<\/svg>\s*$/, '').trim();
    const constName = toIdent(name);
    entries.push({ constName, name, inner });
  }

  const lines = [
    '// auto-generated by scripts/generate-tabler-icons.mjs — do not edit',
    '// To add or remove icons, edit scripts/tabler-icons.json and run `npm run gen:icons`.',
    '',
    'export const TABLER_ICONS = {',
    ...entries.map((e) => `  ${e.constName}: \`${e.inner.replace(/`/g, '\\`')}\`,`),
    '} as const;',
    '',
    'export type TablerIconName = keyof typeof TABLER_ICONS;',
    '',
  ];
  await writeFile(OUT_PATH, lines.join('\n'), 'utf-8');
  console.log(`Wrote ${entries.length} icon(s) to ${OUT_PATH}`);
}

function toIdent(name) {
  return name.split('-').map((p) => p.charAt(0).toUpperCase() + p.slice(1)).join('');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
